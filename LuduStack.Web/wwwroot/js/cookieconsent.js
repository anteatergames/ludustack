function initializeCookieConsent(message, linkText, url) {
    window.cookieconsent.initialise({
        cookie: {
            name: '.LuduStack.Consent'
        },
        palette: {
            popup: {
                background: "#3c404d",
                text: "#d6d6d6"
            },
            button: {
                background: "#8bed4f"
            }
        },
        theme: "classic",
        position: "bottom-left",
        content: {
            message: message,
            link: linkText,
            href: url
        }
    });
}